public class Q5 {
    public static void main(String[] args) {
        int n = 5; // Nombre de lignes
        for (int i = 1; i <= n; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("x"); // Remplacer '*' par 'x'
            }
            System.out.println();
        }
    }
}
